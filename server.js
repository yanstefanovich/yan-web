// Dependencies
const express = require('express');
////////////////////////////////////////////////////////////////////////////////
var app = express();

// Declare port
const port = process.env.PORT || 3000;
////////////////////////////////////////////////////////////////////////////////

// Middleware

////////////////////////////////////////////////////////////////////////////////

// Routes
app.get('/', (req, res) => {
  res.send('Home');
});

app.get('/about', (req, res) => {
  res.send('About');
});

app.get('/projects', (req, res) => {
  res.send('Projects');
});
////////////////////////////////////////////////////////////////////////////////

// Start the app
app.listen(port, () => {
  console.log(`Server up on port ${port}`);
});
